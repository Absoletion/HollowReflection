'use strict';
// Regenerates ../manifest.js from the emitted sprite files, in canonical cast order.
const fs = require('fs');
const path = require('path');
const ORDER = ['hale','hale_awakened','cinnia','tobin','marlowe','brant','nix','brigga','hearthgar','milla',
  'construct','hound','ox','calf','lowingman_beast','lowingman_man','glasswright'];
const dir = path.join(__dirname, '..');
const SPRITES = {};
for (const key of ORDER) {
  const file = path.join(dir, key + '.js');
  if (!fs.existsSync(file)) { console.error('MISSING sprite file: ' + key + '.js'); process.exit(1); }
  new Function('SPRITES', fs.readFileSync(file, 'utf8'))(SPRITES);
}
const rows = ORDER.map(key => {
  const s = SPRITES[key];
  const fc = Object.entries(s.anims).map(([a, fr]) => a + ':' + fr.length).join(',');
  return `  { key:'${key}', w:${s.w}, h:${s.h}, frames:{${fc}} }`;
});
const src = '// manifest.js — generated by gen/make-manifest.js. Key list + frame counts for integrity tests.\n' +
  'const SPRITE_LIST = [\n' + rows.join(',\n') + '\n];\n' +
  "if (typeof module !== 'undefined' && module.exports) { module.exports = SPRITE_LIST; }\n";
fs.writeFileSync(path.join(dir, 'manifest.js'), src);
console.log('wrote manifest.js (' + ORDER.length + ' sprites)');
